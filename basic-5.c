#include<stdio.h>
int main() {
     int  num  = 6;
    // post increment
    printf("%d\n", num++);
    printf("%d\n", num);
    // pre increment

    printf("%d\n", ++num);
    printf("%d\n", num);

    // post decrement
    printf("%d\n", num--);
    printf("%d\n", num);

    // pre decrement

    printf("%d\n", --num);
    printf("%d\n", num);

}
